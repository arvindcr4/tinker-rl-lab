    async def _train_step_remote(self, step: int) -> Dict[str, Any]:
        """Execute training step on remote backend."""
        # For vast.ai, this is handled by the remote script
        return {
            "step": step,
            "message": "Remote training in progress",
            "reward/mean": 0.5,
        }

    async def _train_step_colab(self, step: int) -> Dict[str, Any]:
        """Execute training step in Colab."""
        return {
            "step": step,
            "message": "Colab training - see notebooks/skyrl_training_colab.ipynb",
            "reward/mean": 0.5,
        }

    async def run(self):
        """Main training loop."""
        print("\n" + "=" * 60)
        print("Starting SkyRL Training")
        print("=" * 60 + "\n")

        await self.setup()

        for step in range(self.config.epochs):
            try:
                metrics = await self.train_step(step)
                self.current_step = step + 1

                print(f"\nStep {step} complete - Loss: {metrics.get('loss', 'N/A'):.4f}")

            except Exception as e:
                print(f"Error in step {step}: {e}")
                import traceback
                traceback.print_exc()
                break

        print("\n" + "=" * 60)
        print("Training complete!")
        print(f"Final reward: {self.reward_history[-1] if self.reward_history else 'N/A':.4f}")
        print("=" * 60 + "\n")

        return {
            "final_step": self.current_step,
            "reward_history": self.reward_history,
            "loss_history": self.loss_history,
        }

    def save_checkpoint(self, path: str):
        """Save training checkpoint."""
        checkpoint = {
            "step": self.current_step,
            "config": self.config.to_dict(),
            "reward_history": self.reward_history,
            "loss_history": self.loss_history,
            "best_reward": self.best_reward,
        }
        with open(path, "w") as f:
            json.dump(checkpoint, f, indent=2)
        print(f"Checkpoint saved to {path}")

    def load_checkpoint(self, path: str):
        """Load training checkpoint."""
        with open(path, "r") as f:
            checkpoint = json.load(f)
        self.current_step = checkpoint["step"]
        self.reward_history = checkpoint.get("reward_history", [])
        self.loss_history = checkpoint.get("loss_history", [])
        self.best_reward = checkpoint.get("best_reward", 0.0)
        print(f"Checkpoint loaded from {path}")


