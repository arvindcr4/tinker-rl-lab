"""
SkyRL Trainer for tinker-rl-lab

A wrapper around SkyRL's training framework that supports:
- Local training with vLLM/SGLang
- Tinker API integration
- vast.ai remote training
- Google Colab training

Based on NovaSky-AI/SkyRL's skyrl-train package.
"""

import os
import asyncio
import time
import json
import subprocess
from pathlib import Path
from typing import Dict, Any, List, Optional, Callable

import torch
import numpy as np
import yaml

from skyrl.config import SkyRLConfig


class SkyRLTrainer:
    """
    SkyRL Trainer that coordinates RL training with flexible backends.

    Supports:
    - Local training with vLLM inference
    - Remote training via vast.ai
    - Tinker API integration
    - Google Colab with TPU/GPU
    """

    def __init__(self, config: SkyRLConfig):
        self.config = config

        # Training state
        self.current_step = 0
        self.best_reward = 0.0
        self.training_metrics = []

        # Backend-specific clients
        self.inference_client = None
        self.training_client = None

        # Results tracking
        self.reward_history = []
        self.loss_history = []

    async def setup(self):
        """Initialize training components based on backend."""
        print(f"\n{'='*60}")
        print(f"Setting up SkyRL Trainer")
        print(f"Backend: {self.config.backend.backend}")
        print(f"Model: {self.config.model_name}")
        print(f"Algorithm: {self.config.trainer.algorithm}")
        print(f"{'='*60}\n")

        backend = self.config.backend.backend

        if backend == "tinker":
            await self._setup_tinker()
        elif backend == "vastai":
            await self._setup_vastai()
        elif backend == "colab":
            await self._setup_colab()
        elif backend in ("auto", "local"):
            await self._setup_local()
        else:
            raise ValueError(f"Unknown backend: {backend}")

    async def _setup_tinker(self):
        """Setup Tinker API backend."""
        import tinker
        from tinker.types import AdamParams

        print("Setting up Tinker API backend...")

        self.service_client = tinker.ServiceClient(
            api_key=self.config.backend.tinker_api_key or os.environ.get("TINKER_API_KEY")
        )

        # Create training client with LoRA
        self.training_client = await self.service_client.create_lora_training_client_async(
            base_model=self.config.model_name,
            rank=self.config.lora_rank,
        )

        # Create sampling client for inference
        initial_path = self.training_client.save_weights_for_sampler(name="step_0").result().path
        self.inference_client = self.service_client.create_sampling_client(model_path=initial_path)

        print("Tinker API backend ready")

    async def _setup_vastai(self):
        """Setup vast.ai backend - prepare config for remote execution."""
        print("Setting up vast.ai backend...")
        print("  Note: Actual execution happens via run_skyrl_vast.sh")
        # Configuration is prepared; actual execution is done remotely
        self._prepare_vastai_config()

    async def _setup_colab(self):
        """Setup Google Colab backend."""
        print("Setting up Google Colab backend...")
        print("  Note: Execution via Colab notebooks in skyrl/notebooks/")

    async def _setup_local(self):
        """Setup local training with vLLM."""
        print("Setting up local backend with vLLM...")
        # Import and setup vLLM inference engine
        try:
            from vllm import LLM, SamplingParams
            self._vllm_llm = LLM(
                model=self.config.model_name,
                tensor_parallel_size=self.config.generator.tensor_parallel_size,
                gpu_memory_utilization=self.config.generator.gpu_memory_utilization,
            )
            self._sampling_params = SamplingParams(
                temperature=self.config.generator.temperature,
                top_p=self.config.generator.top_p,
                max_tokens=self.config.generator.max_generate_length,
            )
            print(f"vLLM ready with {self.config.generator.tensor_parallel_size} GPUs")
        except ImportError:
            print("Warning: vLLM not installed. Install with: pip install vllm")
            self._vllm_llm = None

    def _prepare_vastai_config(self):
        """Prepare configuration for vast.ai remote execution."""
        config_path = Path("skyrl_vastai_config.yaml")
        self.config.to_yaml(config_path)
        print(f"  Config saved to {config_path}")

        # Generate the vast.ai run script
        self._generate_vastai_script()

